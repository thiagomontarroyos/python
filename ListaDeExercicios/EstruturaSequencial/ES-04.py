# Faça um Programa que peça as 4 notas bimestrais e mostre a média.

bi1= float(input("Digite a primeira nota bimestral: "))
bi2= float(input("Digite a segunda nota bimestral: "))
bi3= float(input("Digite a terceira nota bimestral: "))
bi4= float(input("Digite a quarta nota bimestral: "))
media = (bi1+bi2+bi3+bi4)/4
print("A sua média bimestral é igual a:", media)